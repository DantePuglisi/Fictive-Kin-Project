import UIKit

extension UIColor {
	struct CustomColor {
		static let mainBackground = UIColor(red: 173/255.0, green: 173/255.0, blue: 173/255.0, alpha: 1.0)
		static let cellBackground = UIColor.white
	}
	
	struct GradientColors {
		static let topLeft = UIColor(red: 255/255.0, green: 146/255.0, blue: 139/255.0, alpha: 1.0)
		static let bottomRight = UIColor(red: 255/255.0, green: 172/255.0, blue: 129/255.0, alpha: 1.0)
	}
}
